void time() {
    cerr << ln << "Time elapsed: " << 1.0 * clock() / CLOCKS_PER_SEC << "s." 
         << ln;
}

void solve() {
    int n; cin >> n;

    int arr[10000];
    int idx = 0;
    for (int i = 1; i * i <= n; ++i) {
        if (n % i == 0) {
            cout << i << ' ';
            if (i != n / i) {
                arr[idx] = n / i;
                ++idx;
            }
    
        }
    }

    while (idx--) cout << arr[idx] << ' ';
}

signed main() {
    docfile();
    fastio();

    int t = 1;
    // cin >> t;
    while (t--) {
        solve();
    }

    time();
    return 0;
}