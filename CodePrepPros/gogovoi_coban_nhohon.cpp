void time() {
    cerr << ln << "Time elapsed: " << 1.0 * clock() / CLOCKS_PER_SEC << "s." 
         << ln;
}

void solve() {
    int n, m;
    cin >> n >> m;
    vector<int> a(n), b(m);
    for (int &x : a) cin >> x;
    for (int &x : b) cin >> x;

    for (int i = 0; i < m; ++i) {
        int cnt = lower_bound(a.begin(), a.end(), b[i]) - a.begin();
        cout << cnt << ' ';
    }
    cout << ln;
}

